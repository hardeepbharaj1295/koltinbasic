class Calculator {

    fun add(a: Int = 0, b: Int = 0): Int {
        return a + b
    }

    fun subtract(a: Int = 0, b: Int = 0): Int {
        return a - b
    }

    fun multiplication(a: Int = 0, b: Int = 0): Int {
        return a * b
    }

    fun division(a: Int = 0, b: Int = 0): Int {
        return a / b
    }
}